# Crie um algorito que leia um numero e mostre o seu dobro, seu triplo e sua raiz quadrada
import math

n1 = int(input('Digite o seu numero de preferencia:'))
dobro = n1 * 2
triplo = n1 * 3
raiz = math.sqrt(n1)
print('Este é o dobro do seu valor', dobro)
print('Este é o triplo do seu valor', triplo)
print('Esta é a raiz quadrada do seu valor', raiz)

